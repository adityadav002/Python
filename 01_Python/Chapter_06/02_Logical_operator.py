# Get input from the user and convert to integer
x = int(input("Enter a number: "))

# AND: Both conditions must be true
print("x > 2 and x < 10:", x > 2 and x < 10)

# OR: At least one condition must be true
print("x < 2 or x < 10:", x < 2 or x < 10)    

#NOT: Reverses the result of the AND condition
print("not(x > 2 and x < 10):", not(x > 2 and x < 10))
